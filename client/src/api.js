import axios from 'axios';

const API = axios.create({
  baseURL: import.meta.env.VITE_API_URL ? `${import.meta.env.VITE_API_URL}/api` : '/api'
});

// Add token to requests
API.interceptors.request.use((config) => {
  const token = localStorage.getItem('token');
  if (token) {
    config.headers.Authorization = `Bearer ${token}`;
  }
  return config;
});

// Auth APIs
export const signup = (data) => API.post('/auth/signup', data);
export const login = (data) => API.post('/auth/login', data);
export const getMe = () => API.get('/auth/me');
export const getAllUsers = () => API.get('/auth/users');

// Project APIs
export const getProjects = () => API.get('/projects');
export const getProject = (id) => API.get(`/projects/${id}`);
export const createProject = (data) => API.post('/projects', data);
export const updateProject = (id, data) => API.put(`/projects/${id}`, data);
export const deleteProject = (id) => API.delete(`/projects/${id}`);
export const addProjectMember = (projectId, data) => API.post(`/projects/${projectId}/members`, data);
export const removeProjectMember = (projectId, userId) => API.delete(`/projects/${projectId}/members/${userId}`);

// Task APIs
export const getMyTasks = () => API.get('/tasks/my-tasks');
export const getProjectTasks = (projectId) => API.get(`/tasks/project/${projectId}`);
export const getTask = (id) => API.get(`/tasks/${id}`);
export const createTask = (data) => API.post('/tasks', data);
export const updateTask = (id, data) => API.put(`/tasks/${id}`, data);
export const deleteTask = (id) => API.delete(`/tasks/${id}`);
export const addComment = (taskId, data) => API.post(`/tasks/${taskId}/comments`, data);
